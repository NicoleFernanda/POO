package lista1;

public class Formas {
	
	protected String cor;
	protected float tamanho = 50;
	 
	public Formas () {
		
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}

	public float getTamanho() {
		return tamanho;
	}

	public void setTamanho(float tamanho) {
		this.tamanho = tamanho;
	}
	
	
	
}
